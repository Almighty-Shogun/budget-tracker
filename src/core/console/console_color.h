#ifndef CONSOLE_COLOR_H
#define CONSOLE_COLOR_H

// Console escape code to reset the color default.
#define CONSOLE_COLOR_RESET "\033[0m"

// Console escape code for red text.
#define CONSOLE_COLOR_RED "\033[31m"

// Console escape code for green text.
#define CONSOLE_COLOR_GREEN "\033[32m"

// Console escape code for yellow text.
#define CONSOLE_COLOR_YELLOW "\033[33m"

// Console escape code for blue text.
#define CONSOLE_COLOR_BLUE "\033[34m"

// Console escape code for magenta text.
#define CONSOLE_COLOR_MAGENTA "\033[35m"

// Console escape code for cyan text.
#define CONSOLE_COLOR_CYAN "\033[36m"

// Console escape code for white text.
#define CONSOLE_COLOR_WHITE "\033[37m"

#endif //CONSOLE_COLOR_H
